package kr.or.ddit.vote.vo;

import java.text.SimpleDateFormat;
import java.util.Date;

import kr.or.ddit.comm.vo.BaseVO;

public class VotePostVO extends BaseVO{

	private int votePostSeq;
	private String empId;
	private String empNm;
	private String votePostTitle;
	private String votePostCont;
	private Date votePostDt;
	private int votePostPartyCnt;
	private int votePostEmpCnt;
	private String votePostSt;
	private Date votePostStartDt;
	private Date votePostEndDt;
	
	
	
	public void setVotePostEmpCnt(int votePostEmpCnt) {
		this.votePostEmpCnt = votePostEmpCnt; 
	}
	public int getVotePostEmpCnt() {
		return votePostEmpCnt; 
	}
	public void setEmpId(String empId) {
		this.empId = empId; 
	}
	public String getEmpId() {
		return empId; 
	}
	public void setVotePostPartyCnt(int votePostPartyCnt) {
		this.votePostPartyCnt = votePostPartyCnt; 
	}
	public int getVotePostPartyCnt() {
		return votePostPartyCnt; 
	}
	public void setEmpNm(String empNm) {
		this.empNm = empNm; 
	}
	public String getEmpNm() {
		return empNm; 
	}
	public void setVotePostCont(String votePostCont) {
		this.votePostCont = votePostCont; 
	}
	public String getVotePostCont() {
		return votePostCont; 
	}
	public void setVotePostTitle(String votePostTitle) {
		this.votePostTitle = votePostTitle; 
	}
	public String getVotePostTitle() {
		return votePostTitle; 
	}
	public void setVotePostDt(Date votePostDt) {
		this.votePostDt = votePostDt; 
	}
	public String getVotePostDt() {
		if(votePostDt != null) {
			return new SimpleDateFormat("yyyy-MM-dd").format(votePostDt);
		}else { return ""; }
	}
	public void setVotePostSt(String votePostSt) {
		this.votePostSt = votePostSt; 
	}
	public String getVotePostSt() {
		return votePostSt; 
	}
	public void setVotePostSeq(int votePostSeq) {
		this.votePostSeq = votePostSeq; 
	}
	public int getVotePostSeq() {
		return votePostSeq; 
	}
	public void setVotePostStartDt(Date votePostStartDt) {
		this.votePostStartDt = votePostStartDt;
	}
	public String getVotePostStartDt() {
		if(votePostStartDt != null) {
			return new SimpleDateFormat("yyyyMMdd").format(votePostStartDt);
		}else { return ""; }
	}
	public void setVotePostEndDt(Date votePostEndDt) {
		this.votePostEndDt = votePostEndDt;
	}
	public String getVotePostEndDt() {
		if(votePostEndDt != null) {
			return new SimpleDateFormat("yyyyMMdd").format(votePostEndDt);
		}else { return ""; }
	}
	

}
